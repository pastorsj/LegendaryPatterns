package legendary.Interfaces;

/**
 * The representation of a pattern
 */
public interface IPattern{
	
	/**
	 * The appropriate tag of a pattern component
	 *
	 * @return the tag
	 */
	public String tag();
	
	/**
	 * The appropriate color of a pattern component
	 *
	 * @return the color
	 */
	public String color();
}
